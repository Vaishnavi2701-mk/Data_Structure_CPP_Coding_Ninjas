#include<iostream>
using namespace std;


        class Node
        {
        public:
	        int data;
	        Node *next;
	        Node(int data)
	        {
		        this->data = data;
		        this->next = NULL;
	        }
        };

Node *takeinput(){
    int data;
	cin >> data;
	Node *head = NULL, *tail = NULL;
	while (data != -1)
	{
		Node *newNode = new Node(data);
		if (head == NULL)
		{
			head = newNode;
			tail = newNode;
		}
		else
		{
			tail->next = newNode;
			tail = newNode;
		}
		cin >> data;
	}
	return head;
}
void deleteAlternateNodes(Node *head){
Node *current=head;
Node *prev=head;
while(current!=NULL && current->next!=NULL){
    prev->next=current->next->next;
    current=current->next;
    prev=prev->next;
}

}
void print(Node *head)
{
	Node *temp = head;
	while (temp != NULL)
	{
		cout << temp->data << " ";
		temp = temp->next;
	}
	cout << endl;
}

int main()
{
	int t;
	cin >> t;

	while (t--)
	{
		Node *head = takeinput();
		deleteAlternateNodes(head);
		print(head);
	}

	return 0;
}
